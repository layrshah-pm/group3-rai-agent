"""
validate_demo.py
----------------
Runs each of the 3 demo scenarios N times and reports:
  - Pass rate per scenario
  - Flaky runs (unexpected outcomes)
  - Timing statistics (mean / min / max per scenario)

Usage:
  python validate_demo.py              # 20 runs each (default)
  python validate_demo.py --runs 5     # faster smoke check
  python validate_demo.py --runs 20 --scenario 1   # single scenario only

Results are written to: validation_report.txt
"""

