def run_once(scenario_id: int) -> dict:
    sc = SCENARIOS[scenario_id]
    state = create_initial_state(**sc["state_kwargs"])
    config = {"configurable": {"thread_id": str(uuid.uuid4())}}

    t0 = time.time()
    result = app.invoke(state, config=config)
    elapsed = round(time.time() - t0, 2)

    fs = result["final_status"]
    violations = set(result.get("violations", []))
    passed = (
        fs in sc["expected_status"]
        and sc["expected_violations_include"].issubset(violations)
        and not sc["expected_violations_exclude"].intersection(violations)
    )

    return {
        "scenario": scenario_id,
        "final_status": fs,
        "violations": violations,
        "elapsed": elapsed,
        "passed_expectations": passed,
    }


def run_scenario(scenario_id: int, n: int) -> dict:
    sc = SCENARIOS[scenario_id]
    print(f"\n  Scenario {scenario_id}: {sc['name']} ({n} runs)")
    print(f"  Expected status: {sc['expected_status']}")

    results = []
    for i in range(1, n + 1):
        print(f"    Run {i:2d}/{n} ... ", end="", flush=True)
        r = run_once(scenario_id)
        mark = "✓" if r["passed_expectations"] else "✗ FLAKY"
        print(f"{r['final_status']:12} {r['elapsed']:6.1f}s  {mark}")
        results.append(r)

    pass_count = sum(1 for r in results if r["passed_expectations"])
    elapsed_vals = [r["elapsed"] for r in results]
    statuses = [r["final_status"] for r in results]
    flaky = [r for r in results if not r["passed_expectations"]]

    summary = {
        "scenario_id": scenario_id,
        "name": sc["name"],
        "total": n,
        "pass_count": pass_count,
        "pass_rate": round(pass_count / n * 100, 1),
        "statuses": statuses,
        "mean_elapsed": round(sum(elapsed_vals) / n, 1),
        "min_elapsed": round(min(elapsed_vals), 1),
        "max_elapsed": round(max(elapsed_vals), 1),
        "flaky_runs": flaky,
    }

    print(f"  → Pass rate: {pass_count}/{n} ({summary['pass_rate']}%)")
    print(f"  → Timing: mean={summary['mean_elapsed']}s  min={summary['min_elapsed']}s  max={summary['max_elapsed']}s")
    if flaky:
        print(f"  → FLAKY RUNS:")
        for f in flaky:
            print(f"       status={f['final_status']}  violations={f['violations']}")
    return summary


def write_report(summaries: list[dict], n: int, outfile: str) -> None:
    lines = []
    lines.append("=" * 65)
    lines.append("  RAI COMPLIANCE AGENT — VALIDATION REPORT")
    lines.append(f"  Generated: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
    lines.append(f"  Runs per scenario: {n}")
    lines.append("=" * 65)

    overall_pass = sum(s["pass_count"] for s in summaries)
    overall_total = sum(s["total"] for s in summaries)
    overall_rate = round(overall_pass / overall_total * 100, 1)

    lines.append(f"\n  OVERALL PASS RATE: {overall_pass}/{overall_total} ({overall_rate}%)")
    lines.append("")

    for s in summaries:
        lines.append(f"  Scenario {s['scenario_id']}: {s['name']}")
        lines.append(f"    Pass rate : {s['pass_count']}/{s['total']} ({s['pass_rate']}%)")
        lines.append(f"    Timing    : mean={s['mean_elapsed']}s  min={s['min_elapsed']}s  max={s['max_elapsed']}s")
        from collections import Counter
        status_dist = Counter(s["statuses"])
        lines.append(f"    Statuses  : {dict(status_dist)}")
        if s["flaky_runs"]:
            lines.append(f"    FLAKY     : {len(s['flaky_runs'])} run(s)")
            for f in s["flaky_runs"]:
                lines.append(f"      → {f['final_status']}  violations={f['violations']}")
        lines.append("")

    lines.append("=" * 65)
    report = "\n".join(lines)
    print(report)

    with open(outfile, "w") as fh:
        fh.write(report + "\n")
    print(f"\n  Report saved → {outfile}")


def main():
    parser = argparse.ArgumentParser(description="Run demo scenarios N times each.")
    parser.add_argument("--runs", type=int, default=20, help="Runs per scenario (default: 20)")
    parser.add_argument("--scenario", type=int, choices=[1, 2, 3, 4], default=None,
                        help="Run a single scenario only")
    args = parser.parse_args()

    scenario_ids = [args.scenario] if args.scenario else [1, 2, 3, 4]

    print(f"\nRAI Compliance Agent — Demo Validation")
    print(f"Scenarios: {scenario_ids}   Runs each: {args.runs}")

    summaries = []
    for sid in scenario_ids:
        summary = run_scenario(sid, args.runs)
        summaries.append(summary)

    write_report(summaries, args.runs, "validation_report.txt")


if __name__ == "__main__":
    main()
